#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ans;
	unsigned int	i;

	i = 0;
	ans = s;
	while (i < n)
	{
		*(ans + i) = '\0';
		i++;
	}
}
