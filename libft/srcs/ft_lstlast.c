#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*prev;

	if (lst == 0x0)
		return (0x0);
	while (lst)
	{
		prev = lst;
		lst = lst->next;
	}
	return (prev);
}
