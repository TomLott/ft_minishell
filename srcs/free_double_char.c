#include "minishell.h"

void	free_double_char(char ***arr)
{
	int		i;

	i = 0;
	while ((*arr) && (*arr)[i++])
	{
		free((*arr)[i - 1]);
		(*arr)[i - 1] = 0x0;
	}
	free((*arr));
	(*arr) = 0x0;
}
